void PrintHexArray(const char* cArrayName, const unsigned char* bufferData, size_t bufferSize) {
    printf("\nunsigned char %s[%zu] = {", cArrayName, bufferSize);

    for (size_t x = 0; x < bufferSize; x++) {
        if (x % 16 == 0)
            printf("\n\t");

        if (x == bufferSize - 1)
            printf("0x%0.2X", bufferData[x]);
        else
            printf("0x%0.2X, ", bufferData[x]);
    }

    printf("\n};\n");
}

int main(int argc, char* argv[]) {
    if (argc != 2) {
        fprintf(stderr, "Usage: %s <filename>\n", argv[0]);
        return 1;
    }

    FILE* file = fopen(argv[1], "rb");
    if (!file) {
        fprintf(stderr, "Error opening file: %s\n", argv[1]);
        return 1;
    }

    fseek(file, 0, SEEK_END);
    size_t fileSize = ftell(file);
    fseek(file, 0, SEEK_SET);

    unsigned char* bufferData = (unsigned char*)malloc(fileSize);
    if (!bufferData) {
        fprintf(stderr, "Memory allocation error\n");
        fclose(file);
        return 1;
    }

    fread(bufferData, 1, fileSize, file);
    fclose(file);

    PrintHexArray("fileData", bufferData, fileSize);

    free(bufferData);
    return 0;
}
